public class Animal {
    private String species;

    public Animal() {
        this.species = species;
    }
    public Animal (String newSpecies) {
        this.species = newSpecies;
    }
    public void setSpecies(String newSpecies) {
        this.species = newSpecies;
    }
    public String getSpecies() {
        return this.species;
    }
    public String toString() {
        return this.species;
    }    
}
